    private void btInserirMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btInserirMActionPerformed

        String nome = tbNome.getText();

        if (nome.equals("")) {
            JOptionPane.showMessageDialog(null, "Digite um nome");
        } else {
            int i = c.pesquisarMedicamento(nome);
            if (i < c.quant) {
                JOptionPane.showMessageDialog(null, "Medicamento já cadastrado");
                limparCampos();
                desabilitarCampos();
                btInserirM.setEnabled(false);
            } else {
                Medicamento m = new Medicamento();

                m.setCodigo(Integer.parseInt(tbCodigo.getText()));//colocar o codigo dentro de tbCodigo.
                m.setNome(tbNome.getText());//colocar o nome dentro de tbNome.
                m.setLaboratorio(tbLaboratorio.getText());//colocar o laboratorio dentro de tbLaboratorio.
                m.setQuantidade(Integer.parseInt(tbQuantidade.getText()));//colocar quantidade dentro de lbQuantidade.
                m.setPreco(Double.parseDouble(tbPreco.getText()));//colocar preco dentro de tbPreco.

                if (c.quant < 10) { //verificar se o meu carrinho é menor que 10.
                    c.med[c.quant] = m; //se não tiver lotado...
                    c.quant++; //ele insere o meu medicamento no carrinho.

                    JOptionPane.showMessageDialog(null, "Medicamento inserido");
                } else {
                    JOptionPane.showMessageDialog(null, "Carrinho Lotado");
                }
                //Exibindo informações do carrinho
                for (i = 0; i < c.quant; i++) {
                    m = c.med[i];
                    m.exibirMedicamento();
                }
            }
            limparCampos();
            desabilitarCampos();
            btInserirM.setEnabled(false);
            btCancelar.setEnabled(false);
            btInserir.setEnabled(true);
        }
    }//GEN-LAST:event_btInserirMActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        String nome = tbNome.getText();

        if (nome.equals("")) { //se o preenchimento for vazio faça.
            JOptionPane.showMessageDialog(null, "Digite um nome");
        } else {//se o preenchimento não for vazio.
            int i = c.pesquisarMedicamento(nome); //verificar se existe o medicamento.
            if (i < c.quant) {//se(sim) achar o medicamento faça.
                String r = JOptionPane.showInputDialog(null, "Confirma exclusão [S|N]?");
                if (r.equals("S")) {
                    //Copiar o ultimo medicamento para a posição i
                    c.quant--; //diminuir
                    c.med[i] = c.med[c.quant]; //substituir o ultimo para a posição i
                    JOptionPane.showMessageDialog(null, "Medicamento Excluido");
                    for (i = 0; i < c.quant; i++) {
                        c.med[i].exibirMedicamento();
                        limparCampos();
                        btAlterar.setEnabled(false);
                        btExcluir.setEnabled(false);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Medicamento não Excluido");
                    limparCampos();
                    btAlterar.setEnabled(false);
                    btExcluir.setEnabled(false);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Medicamento não registrado");
            }
        }
    }//GEN-LAST:event_btExcluirActionPerformed

