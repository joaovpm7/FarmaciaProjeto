    private void btPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisarActionPerformed
        desabilitarCampos();
        btInserirM.setEnabled(false);
        btInserir.setEnabled(true);
        String nome = tbNome.getText();

        if (nome.equals("")) { //se o preenchimento for vazio faça.
            JOptionPane.showMessageDialog(null, "Digite um nome");
        } else {//se o preenchimento não for vazio.
            int i = c.pesquisarMedicamento(nome); //verificar se existe o medicamento.
            if (i < c.quant) {//se(sim) achar o medicamento faça.
                //Passar os dados do medicamento para a tela
                Medicamento m = c.med[i];//medicamento no vetor.
                tbCodigo.setText(String.valueOf(m.getCodigo()));//colocar o codigo na tela.
                tbLaboratorio.setText(m.getLaboratorio());//colocar o laboratorio na tela.
                tbQuantidade.setText(String.valueOf(m.getQuantidade()));//colocar a quantidade na tela.
                DecimalFormat df = new DecimalFormat("#,00");//usar o decimalformat para o preço.
                tbPreco.setText(df.format(m.getPreco()));//colocar preco na tela.
                btAlterar.setEnabled(true);
                btExcluir.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(null, "Medicamento não registrado");
                limparCampos();
            }
        }
    }//GEN-LAST:event_btPesquisarActionPerformed

    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed

        Medicamento m = new Medicamento();
        m.setCodigo(Integer.parseInt(tbCodigo.getText()));
        m.setNome(tbNome.getText());
        m.setLaboratorio(tbLaboratorio.getText());
        m.setQuantidade(Integer.parseInt(tbQuantidade.getText()));
        m.setPreco(Double.parseDouble(tbPreco.getText()));
        c.med[pos] = m;
        JOptionPane.showMessageDialog(null, "Medicamento alterado");
        c.med[pos].exibirMedicamento();

        //desabilitando campos da tela Alterar
        desabilitarCampos();
        limparCampos();
        btAlterar.setEnabled(false);
        btCancelar.setEnabled(false);
        btInserir.setEnabled(true);
    }//GEN-LAST:event_btSalvarActionPerformed

